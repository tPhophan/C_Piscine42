int	ft_sqrt(int nb)
{
	int	i;

	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	else if (nb > 2147395600)
		return (0);
	i = 2;
	while (i <= nb / 2)
	{
		if ((i * i) == nb)
			return (i);
		i += 2;
	}
	i = 1;
	while (i <= nb / 2)
	{
		if ((i * i) == nb)
			return (i);
		i += 2;
	}
	return (0);
}
